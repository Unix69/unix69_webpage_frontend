function DirectoryNode({ node, level }) {
  const [open, setOpen] = useState(level < 1);

  const isFolder = node.type === 'folder';

  return (
    <div className="ml-2">
      <button
        onClick={() => setOpen(!open)}
        className="w-full flex items-center gap-3 text-left px-4 py-3 rounded-2xl hover:bg-white/5 transition-all"
      >
        {isFolder ? (
          open ? (
            <ChevronDown className="w-5 h-5 text-zinc-400" />
          ) : (
            <ChevronRight className="w-5 h-5 text-zinc-400" />
          )
        ) : null}

        {isFolder ? (
          <Folder className="w-5 h-5 text-cyan-400" />
        ) : (
          <File className="w-5 h-5 text-zinc-400" />
        )}

        <span className={`${level === 0 ? 'font-bold text-lg' : ''}`}>
          {node.name}
        </span>
      </button>

      {isFolder && open && node.children && (
        <div className="ml-8 border-l border-white/10 pl-4 space-y-1 mt-2">
          {node.children.map((child, index) => (
            <div key={index}>
              {child.type === 'folder' ? (
                <DirectoryNode node={child} level={level + 1} />
              ) : (
                <FileItem file={child} />
              )}
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

