function buildTree(dirPath) {
  const stats = fs.statSync(dirPath);

  if (stats.isFile()) {
    return {
      name: path.basename(dirPath),
      type: 'file',
      path: dirPath.replace('public', ''),
    };
  }

  return {
    name: path.basename(dirPath),
    type: 'folder',
    children: fs
      .readdirSync(dirPath)
      .map(child => buildTree(path.join(dirPath, child))),
  };
}

