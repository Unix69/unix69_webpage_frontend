async function bookSlot(slot) {
  setBookingLoading(true);

  try {
    const idempotencyKey = crypto.randomUUID();

    // 1. RESERVE SLOT
    const reserveRes = await fetch("/api/cal/reserve-slot", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        start: slot.startTime,
        idempotencyKey,
      }),
    });

    if (!reserveRes.ok) {
      const err = await reserveRes.json();
      alert(err.error || "Slot not available");
      return;
    }

    const { token } = await reserveRes.json();

    // 2. BOOK SLOT
    const bookRes = await fetch("/api/cal/book", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        start: slot.startTime,
        end: slot.endTime,
        name: "Student User",
        email: "student@email.com",
        title: "Lesson Booking",
        lockToken: token,
        idempotencyKey,
      }),
    });

    if (!bookRes.ok) {
      const err = await bookRes.json();
      alert(err.error || "Booking failed");
      return;
    }

    const data = await bookRes.json();

    // 3. SUCCESS ONLY
    window.open(CAL_TUTORING_LINK, "_blank");

  } catch (err) {
    alert(err.message || "Unexpected error");
  } finally {
    setBookingLoading(false);
  }
}

